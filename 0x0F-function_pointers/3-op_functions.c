#include "3-calc.h"

/**
 * op_add - a function that adds integers
 * @a: first integer
 * @b: second integer
 *
 * Return: sum of integers
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - a function that substructs two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: the difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - a function that multiplies two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: the product
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - a function that divides two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: The quotient
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - a function that remainder after division
 * @a: first integer
 * @b: second integer
 *
 * Return: remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
